/**
 * A function which returns the sum of 2 numbers
 *
 * @param a - The first input number
 * @param b - The second input number
 * @returns a + b
 *
 * @example
 * Here's an example:
 *
 * ```
 * console.log(sample(1,1));  // Prints 2:
 * ```
 */
export function sample(a: number, b: number) {
  return a + b;
}
